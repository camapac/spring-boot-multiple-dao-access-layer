package com.sg.oddle.weather.domain;

public class WeatherWind {

	private Double speed;
	private Double deg;
	
	public Double getSpeed() {
		return speed;
	}
	public void setSpeed(Double speed) {
		this.speed = speed;
	}
	public Double getDeg() {
		return deg;
	}
	public void setDeg(Double deg) {
		this.deg = deg;
	}
	
	
}
